function BFS_AppModes( app ) {
    
    switch ( app.flags.applicationMode ) {
        
        case 'shell':
            break;
        
        case 'desktop':
            break;
        
        case 'open':
            break;
        
        case 'save':
            break;
        
        case 'folder':
            break;
        
    }
    
}